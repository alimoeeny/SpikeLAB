function leg = GetLegends(ft)
switch upper(ft)
    case 'TWO'
        leg = {
            'Pref bd Pref dx (all)',...
            'Pref bd null dx (all)',...
            'Pref bd ZERO dx',...
            'Pref bd ZERO dx (correct response)',...
            'null bd ZERO dx (correct response)',...
            'null bd ZERO dx',...
            'null bd Pref dx(flip)',...
            'Pref bd Pref dx', ...
            'null bd null dx ',...
            'pref bd null dx(flip)',...
            'null bd null dx'};
    case 'DID'
        leg = {
            'Preferred Id', ... 
            'Null Id', ...
            'Pref Id Correct', ... 
            'Null Id Correct', ...
            'Pref Id Wrong', ...
            'Null Id Wrong', ...
            'Pref dx', ...
            'Null dx', ...
            'pref near zero/p choice', ...
            'pref near zero/n choice', ...
            'null near zero/p choice', ...
            'null near zero/n choice', ...
            'Nearest zero Pref dx', ...
            'Nearest zero null dx', ...
            'All trials put together',...%15
            'Next to 0 dx P - response p', ...
            'Next to 0 dx P - response n', ...
            'Next to 0 dx N - response p', ...
            'Next to 0 dx N - response n', ...
            'Pref-Id Correct upper half', ...
            'Pref-Id Correct lower half', ...
            'Null-Id Correct upper half', ...
            'Null-Id Correct lower half'}; %, 'Pref dx Top', 'Null dx Bottom'};
        %leg = {'Preferred Id', 'Null Id', 'Pref Id Correct', 'Null Id Correct', '-', 'Pref Id Wrong', '-', 'Null Id Wrong' }; %, 'Pref dx Top', 'Null dx Bottom'};
    case 'BDID'
        leg = {
            'Pref bd Pref Id', ...
            'Pref bd null Id',...
            'Pref bd Pref Id P', ...
            'Pref bd Pref Id and incorrect response', ...
            'null bd Null Id C', ...
            'null bd null Id incorrect response', ...
            'Pref Id', ...
            'null Id',...
            'p bd',...
            'n bd',...
            'min bd',...
            'max bd'};
    case 'RID'
        leg = {'Pref Or Pref dx','Pref Or null dx','Null Or Pref dx ','null Or null dx'};
    case 'DIDB'
        leg = {'Preferred Id', 'Null Id', 'Pref Id Correct', 'Null Id Correct', 'Pref Id Wrong', 'Null Id Wrong', 'Pref dx', 'Null dx' }; %, 'Pref dx Top', 'Null dx Bottom'};
    otherwise
        leg = {'Preferred Id', 'Null Id', 'Biased twoard pref', 'Biased toward Null'};
end
